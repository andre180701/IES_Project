package com.FastTravel.FastTravelService.inputsForms;

public class FilterIdentifiers {
    private String registration;
    private String identifier;
    private String classe;
    private String meansPayment;
    private String state;


    public String getRegistration() {
        return this.registration;
    }

    public void setRegistration(String registration) {
        this.registration = registration;
    }

    public String getIdentifier() {
        return this.identifier;
    }

    public void setIdentifier(String identifier) {
        this.identifier = identifier;
    }

    public String getClasse() {
        return this.classe;
    }

    public void setClasse(String classe) {
        this.classe = classe;
    }

    public String getMeansPayment() {
        return this.meansPayment;
    }

    public void setMeansPayment(String meansPayment) {
        this.meansPayment = meansPayment;
    }

    public String getState() {
        return this.state;
    }

    public void setState(String state) {
        this.state = state;
    }


}
